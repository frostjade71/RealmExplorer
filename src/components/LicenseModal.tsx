export function LicenseModal({ isOpen, onClose, licenseType, customUrl }: LicenseModalProps) {
  const [content, setContent] = useState<string>('')
  const [isLoading, setIsLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)

  useEffect(() => {
    if (!isOpen) return

    setContent('')
    setError(null)

    if (licenseType === 'Custom' && customUrl) {
      setIsLoading(true)
      fetch(customUrl)
        .then(res => {
          if (!res.ok) throw new Error('Failed to fetch license content')
          return res.text()
        })
        .then(text => setContent(text))
        .catch(err => setError(err.message))
        .finally(() => setIsLoading(false))
    } else {
      setContent(STANDARD_LICENSES[licenseType] || 'License information not available.')
    }
  }, [isOpen, licenseType, customUrl])

  return createPortal(
    <AnimatePresence>
      {isOpen && (
        <div className="fixed inset-0 z-[100] flex items-center justify-center p-4">
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            onClick={onClose}
            className="absolute inset-0 bg-black/80 backdrop-blur-md"
          />

          <motion.div
            initial={{ opacity: 0, scale: 0.9, y: 20 }}
            animate={{ opacity: 1, scale: 1, y: 0 }}
            exit={{ opacity: 0, scale: 0.9, y: 20 }}
            className="relative bg-zinc-950 border border-white/10 w-full max-w-2xl rounded-xl shadow-2xl overflow-hidden max-h-[85vh] flex flex-col"
          >
            <div className="p-6 border-b border-white/5 flex justify-between items-center bg-zinc-900/50">
              <h2 className="font-pixel text-white text-base md:text-lg uppercase tracking-wider flex items-center gap-3">
                <Scale className="w-5 h-5 text-realm-green" />
                {licenseType} License
              </h2>
              <button 
                onClick={onClose}
                className="text-white/40 hover:text-white transition-colors p-2"
              >
                <X className="w-5 h-5" />
              </button>
            </div>

            <div className="p-6 overflow-y-auto flex-1 font-body text-zinc-300 whitespace-pre-wrap text-sm leading-relaxed scrollbar-thin scrollbar-thumb-zinc-700 scrollbar-track-transparent">
              {isLoading ? (
                <div className="flex flex-col items-center justify-center h-32">
                  <LoadingSpinner />
                  <p className="mt-4 text-xs font-headline uppercase tracking-widest text-zinc-500">Loading License...</p>
                </div>
              ) : error ? (
                <div className="text-red-400 text-center py-8">
                  <p>Could not load custom license.</p>
                  <a href={customUrl || '#'} target="_blank" rel="noreferrer" className="mt-4 inline-flex items-center gap-2 text-blue-400 hover:underline font-bold text-xs uppercase tracking-widest">
                    View Original File <ExternalLink className="w-3 h-3" />
                  </a>
                </div>
              ) : (
                content
              )}
            </div>
            
            {licenseType === 'Custom' && customUrl && !error && !isLoading && (
              <div className="p-4 bg-zinc-900/50 border-t border-white/5 flex justify-end">
                <a href={customUrl} target="_blank" rel="noreferrer" className="flex items-center gap-2 text-zinc-400 hover:text-white text-xs font-headline uppercase tracking-widest transition-colors font-bold">
                  View Original File <ExternalLink className="w-3 h-3" />
                </a>
              </div>
            )}
          </motion.div>
        </div>
      )}
    </AnimatePresence>,
    document.body
  )
}
